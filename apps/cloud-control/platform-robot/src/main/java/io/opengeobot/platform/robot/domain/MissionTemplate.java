package io.opengeobot.platform.robot.domain;

import com.baomidou.mybatisplus.annotation.IdType;
import com.baomidou.mybatisplus.annotation.TableField;
import com.baomidou.mybatisplus.annotation.TableId;
import com.baomidou.mybatisplus.annotation.TableName;
import io.opengeobot.platform.robot.config.JsonbStringTypeHandler;

import java.time.OffsetDateTime;

/**
 * Persistent mission template entity backed by the
 * {@code mission.mission_template} table. The {@code steps} field stores a
 * JSON array blueprint of step definitions as a JSONB document. Templates are
 * used to fast-create missions with a standard plan.
 */
